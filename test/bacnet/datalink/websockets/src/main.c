static bool cmp(uint8_t *buf, size_t bufsize, uint8_t val)
{
    for (int i = 0; i < bufsize; i++) {
        if (buf[i] != val) {
            return false;
        }
    }
    return true;
}

static void *cli_th_simple_test(void *arg)
{
    BACNET_WEBSOCKET_RET ret;
    BACNET_WEBSOCKET_HANDLE h;
    char url[128];
    uint8_t buf[BACNET_CLIENT_WEBSOCKET_RX_BUFFER_SIZE];
    size_t bytes_received = 0;
    sprintf(url, "wss://%s:%d", BACNET_WEBSOCKET_SERVER_ADDR,
        BACNET_WEBSOCKET_SERVER_PORT);

    ret = cli->bws_connect(BACNET_WEBSOCKET_DIRECT_CONNECTION, url, ca_cert,
        sizeof(ca_cert), client_cert, sizeof(client_cert), client_key,
        sizeof(client_key), &h);
    zassert_equal(ret, BACNET_WEBSOCKET_SUCCESS, NULL);
    zassert_not_equal(h, BACNET_WEBSOCKET_INVALID_HANDLE, NULL);

    while (bytes_received != sizeof(buf)) {
        size_t r;
        ret = cli->bws_recv(h, &buf[bytes_received],
            sizeof(buf) - bytes_received, &r, BACNET_WEBSOCKET_TIMEOUT);
        zassert_equal(ret, BACNET_WEBSOCKET_SUCCESS, NULL);
        bytes_received += r;
    }

    zassert_equal(bytes_received, sizeof(buf), NULL);
    zassert_equal(cmp(buf, sizeof(buf), 0x77), true, NULL);
    memset(buf, 0x99, sizeof(buf));
    ret = cli->bws_send(h, buf, sizeof(buf));
    zassert_equal(ret, BACNET_WEBSOCKET_SUCCESS, NULL);
    // wait a bit while all data is transfered to server
    sleep(1);
    ret = cli->bws_disconnect(h);
    zassert_equal(ret, BACNET_WEBSOCKET_CLOSED, NULL);
    return NULL;
}

